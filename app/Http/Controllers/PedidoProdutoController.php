    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Pedido $pedido)
    {
        $regras = [
            'produto_id' => 'exists:produtos,id',
            'quantidade' => 'required'
        ];

        $feedback = [
            'produto_id.exists' => 'O produto informado não existe',
            'required' => 'O campo :attribute é obrigatório'
        ];

        $request->validate($regras, $feedback);

        $pedido_produto = new PedidoProduto();
        $pedido_produto->pedido_id = $pedido->id;
        $pedido_produto->produto_id = $request->produto_id;
        $pedido_produto->quantidade = $request->quantidade;
        $pedido_produto->save();

        return redirect()->route('pedido_produto.create', ['pedido' => $pedido->id]);
    }

